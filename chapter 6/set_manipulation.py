first = {'red', 'green', 'blue'}
second = {'cyan', 'green', 'blue', 'magenta', 'red'}

print(first == second)
print(first != second)
print(first > second)
print(first < second)
print(first >= second)
print(first <= second)

#combine with set operators
print(first | second)
print(first & second)
print(first - second)
print(first ^ second)